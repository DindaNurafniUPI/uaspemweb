<?php
include 'functions.php';
$pdo = pdo_connect_mysql();
$msg = '';
// Check if the contact id exists, for example update.php?id=1 will get the contact with the id of 1
if (isset($_GET['id'])) {
    if (!empty($_POST)) {
        // This part is similar to the create.php, but instead we update a record and not insert
        $id = isset($_POST['id']) ? $_POST['id'] : NULL;
        $nama = isset($_POST['nama']) ? $_POST['nama'] : '';
        $email = isset($_POST['email']) ? $_POST['email'] : '';
        $komentar = isset($_POST['komentar']) ? $_POST['komentar'] : '';
        $id_berita = isset($_POST['id_berita']) ? $_POST['id_berita'] : '';
        
        // Update the record
        $stmt = $pdo->prepare('UPDATE komentar SET id = ?, nama = ?, email = ?, komentar = ?, id_berita = ? WHERE id = ?');
        $stmt->execute([$id, $nama, $email, $komentar, $id_berita, $_GET['id']]);
        $msg = 'Updated Successfully!';
    }
    // Get the contact from the contacts table
    $stmt = $pdo->prepare('SELECT * FROM komentar WHERE id = ?');
    $stmt->execute([$_GET['id']]);
    $contact = $stmt->fetch(PDO::FETCH_ASSOC);
    if (!$contact) {
        exit('Komen doesn\'t exist with that ID!');
    }
} else {
    exit('No ID specified!');
}
